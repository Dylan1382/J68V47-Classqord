public class Main {

    public static void main(String[] args) {

        String[] line1 = {"Flakes fall softly down",
                "Crystal flakes drifting"};

        String[] line2 = {"Blanketing the ground in white",
                "Tree branches wear icy gowns"};

        String[] line3 = {"Winter's cold beauty",
                "Snow covers all still"};

        for (int i = 0; i < 2; i++) {
            System.out.println(line1[i]);
            System.out.println(line2[i]);
            System.out.println(line3[i]);
            System.out.println();
        }
    }

}